    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (cajaId.getText().length()==0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe Seleccionar Un Articulo");
            cajaId.requestFocus();
            
        }
        if(cajaCantidad.getText().length()==0)
        {
        
            JOptionPane.showConfirmDialog(rootPane, "Debe Ingresar la cantidad a vender");
        
            cajaCantidad.requestFocus();
        
            return;
        }
        // SI NO ESTA VACIO VERIFICAMOS QUE HAYA EN STOCK
        int codigoProducto = Integer.parseInt(cajaId.getText());
        int cant = Integer.parseInt(cajaCantidad.getText());
        int stock = Integer.parseInt(cajaStock.getText());
   
        if((stock >= cant) && (cant!=0))
        {
          String valor[] = new String[4];
    
          double precio=Double.parseDouble(cajaUnitario.getText());
          
          double importe =cant*precio;
         
          String imp =String.valueOf(importe);
         
          total=total+importe;
         
          String tot = String.valueOf(total);
         
          m=(DefaultTableModel) tablaVentas.getModel();
        
          valor[0]=cajaDescripcion.getText();
        
          valor[1]=cajaCantidad.getText();
        
          valor[2]=cajaUnitario.getText();

        
          valor[3]=imp;
        
          totalFilas++;
        
          totalFilasCargadas.setText(String.valueOf(totalFilas));
        
          cajaTotal.setText(tot);
        
          m.addRow(valor);
        
          limpiar();
        
          btnProducto.requestFocus();
        
         }
        else
        {
        JOptionPane.showMessageDialog(this, "La cantidad Solicitada Excede al Stock Disponible , Disponible "+stock, "ADVERTENCIA", JOptionPane.CANCEL_OPTION);
        
        cajaCantidad.requestFocus();
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

