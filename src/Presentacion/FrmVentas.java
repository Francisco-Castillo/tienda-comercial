    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
         
            DefaultTableModel m = (DefaultTableModel) tablaVentas.getModel();
            
            int filas = m.getRowCount();
           
            sSQL="INSERT INTO v(descripcion,cantidad,precio,importe)VALUES(?,?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sSQL);
            for (int i = 0; i < filas; i++) {
                
                
                // OBTENEMOS DATOS DE FILAS
                String descripcion = (String) m.getValueAt(i, 0);
                
                int cantidad = Integer.parseInt(m.getValueAt(i, 1).toString()) ;
                
                double precio = Double.parseDouble(m.getValueAt(i, 2).toString());
                
                double importe = Double.parseDouble(m.getValueAt(i, 3).toString());
                
                pst.setString(1,descripcion);
                
                pst.setInt(2, cantidad);
                
                pst.setDouble(3, precio);
                
                pst.setDouble(4, importe);
                
                pst.executeUpdate();
                
            }
         
           
        } catch (Exception e) {
             JOptionPane.showConfirmDialog(null,e);
             
        }
         prepararTabla();
         cajaTotal.setText("");
        cajaPagoCon.setText("");
        cajaVuelto.setText("");
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

